fun main(args: Array<String>) {
    var a : Boolean = true  // Has only two value
    var b : Char = 'R'     // used to declare  character
    var c : Byte = 12
    var d : Short = -356
    var e : Int = 1234      // Used to represent numeric value generally
    var f : Long = 4536987L  // Used for long and L is used to represent it
    var g : Float = 5.356980F
    var h : Double = 7.346357879985266

    println(a)
    println(b)
    println(c)
    println(d)
    println(e)
    println(f)
    println(g)
    println(h)
}