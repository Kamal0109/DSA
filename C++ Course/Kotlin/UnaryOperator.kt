fun main(args: Array<String>) {
    var number: Double = 2.5

    println(+number)       // here we will print directly 2.5
    println(-number)       // Here we will -(2.5)
    println(++number)      // 2.5 + 1 = 3.5
    println(--number)      // 3.5 - 1 = 2.5
    println(number++)      // It will print the value 2.5 and store it as 3.5
    println(number--)      // It will print the value as 3.5 and store it as 2.5
    println(number)        // It will print your value as 2.5
}