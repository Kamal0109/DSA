fun main(args: Array<String>) {
    var number1: Double = 20.0
    var number2: Double = 10.0
    var result: Double = 0.0

    result = number1 + number2
    println("The result is " + result)

    result = number1 - number2
    println("The result is " + result)

    result = number1 * number2
    println("The result is " + result)

    result = number1 / number2
    println("The result is " + result)

    result = number1 % number2
    println("The result is " + result)
}